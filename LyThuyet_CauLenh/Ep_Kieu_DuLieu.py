# Ép sang kiểu số nguyên: int()
# Ví dụ: 
s1 = '123'
print("s1 = ",type(s1))
a = int(s1) # Cú pháp 
print("a = ",type(a))

print("\n")

# Ép sang kiểu số thực: float()
# Ví dụ: 
s2 = 50
print("s2 = ",type(s2))
a2 = float(s2) # Cú pháp
print("a2 = ",type(a2))

print("\n")

# Ép sang kiểu xâu ký tự: str()
# Ví dụ: 
s3 = 123
print("s3 = ",type(s3))
a3 = str(s3)
print("a3 = ",type(a3))

